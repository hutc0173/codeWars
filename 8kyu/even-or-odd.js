// Create a function that takes an integer as an argument and returns "Even" for even numbers or "Odd" for odd numbers.

// get the number
// if divisible by 2 return even
// if not odd
// loop through

function evenOrOdd(number) {
  if (number % 2 === 0) {
    return "Even";
  } else {
    return "Odd";
  }
}
console.log(evenOrOdd(4, 6, 8));
